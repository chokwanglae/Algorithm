/*
1, 13 >> (1)*7+ (0)*6
2, 13 >> (2)*7+ (0)*6
3, 13 >> (2)*7+ (1)*6
4, 13 >> (2)*7+ (2)*6
5, 13 >> (3)*7+ (2)*6
6, 13 >> (4)*7+ (2)*6
7, 13 >> (4)*7+ (3)*6
8, 13 >> (4)*7+ (4)*6
 */
import java.util.*;
public class Main {
   public static void main(String[] args) {
	   Scanner sc = new Scanner(System.in);
	   int T = sc.nextInt();
	   for(int i=1; i<=T; i++) {
		   int N = sc.nextInt();
		   int M = sc.nextInt();
		   
		   int n1=0,n2=0;
		   int m1=0,m2=0;

		   n1 = (N/4+1)*2;
		   if(N%4==0)
			   n1 -= 2;
		   if(N%4==1)
			   n1 -= 1;
		   n2 = N-n1;

		   m1 = (M/4+1)*2;
		   if(N%4==0)
			   n1 -= 2;
		   if(M%4==1)
			   m1 -= 1;
		   m2 = M-m1;
		   
		   int ans = n1*m1 + n2*m2;
		   System.out.println("#"+i+" "+ans);
	   }
   }
}
